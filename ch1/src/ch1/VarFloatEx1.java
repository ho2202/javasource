public class VarFloatEx1 {
    public static void main(String[] args) {
        float score1 = 90.14F;
        float score = 2.2453463453525F;
        
        double score2 = 98.5D;

        System.out.println(score1);
        System.out.printf("%.2f",score2);
        System.out.printf("%.2f",score);
    }
}
