package loop;

public class WhileEx2 {
    public static void main(String[] args) throws Exception {
        int i = 0;
        while (i < 11){
            System.out.print(i + "   ");
            i++;
        }
        System.out.println();

        i = 10;
        while (i > 0){
            System.out.print(i + "   ");
            i++;
        }
        System.out.println();

    }
}
