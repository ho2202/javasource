package api.math;

public class MathEx1 {
    public static void main(String[] args) {
        double val = 90.49;
        System.out.println("random(): " + Math.random());
        System.out.println("ceil(x): " + Math.ceil(val));
        System.out.println("floor(x): " + Math.floor(val));
        System.out.println("round(x): " + Math.round(val));
        System.out.println("min(x, y): " + Math.min(val, 94.88));
    }
}
